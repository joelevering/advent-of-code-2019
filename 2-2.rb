def compute(input)
  og = {}
  input.split(",").each_with_index do |n, i|
    og[i] = n.to_i
  end

  noun = 0
  verb = 0
  for noun in 0..99 do
    for verb in 0..99 do
      h = og.dup

      h[1] = noun
      h[2] = verb

      if run(h) == 19690720
        puts 100 * noun + verb
      end
    end
  end
end

def run(h)
  l = 0

  while true do
    if h[l] == 99
      break
    elsif h[l] == 1
      res = h[h[l+1]] + h[h[l+2]]
      h[h[l+3]] = res
    elsif h[l] == 2
      res = h[h[l+1]] * h[h[l+2]]
      h[h[l+3]] = res
    end

    l += 4
  end
  h[0]
end

